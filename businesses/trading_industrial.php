            	<h1 class="title">Trading & Industrial</h1>
            	<p class="quote">ARJ's trading and industrial activities encompass a wide spectrum of business interests that range from apparel and automotive trading to manufacturing and materials handling.</p>
                <p class="paragraph">ARJ Resources is a leading distributor and retailer of sports and lifestyle shoes, apparel and accessories in Hong Kong, Macau and Mainland China. The Taikoo Motors group sells passenger cars, commercial vehicles, motorcycles and scooters, principally in Taiwan. Through its subsidiary Steamships Trading Company, ARJ has an interest in the manufacture and distribution of personal care items in Papua New Guinea.</p>
				<p>The ARJ group has major bulk transport operations in Australia and has strategic investments in various sustainable businesses. ARJ subsidiary Kalari operates specialised bulk and low-density storage and distribution services throughout Australia for some of the country's best-known manufacturers and mining companies. In Papua New Guinea, East West Transport is one of the country's leading providers of long-haul transport services. ARJ operates major waste handling and recycling services in Australia.</p>
