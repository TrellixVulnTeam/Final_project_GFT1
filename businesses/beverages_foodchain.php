            	<h1 class="title">Beverages & Food Chain</h1>
            	<p class="quote">ARJ has been involved in food production and distribution since the 1880s, when it established the Taikoo Sugar Refinery in Hong Kong. Today, group companies play an important role in numerous links in the food chain - from primary production to processing, logistics and distribution.</p>
                <p class="paragraph">ARJ's tea growing and trading interests date back to 1976, when it took a minority shareholding in James Finlay, one of the world's oldest tea growers. Finlays is now a wholly owned ARJ subsidiary, and a leading international supplier of tea, coffee and natural ingredients for the global beverage industry.</p>
                <p>ARJ has been a Coca-Cola franchisee since the mid-1960s, and soft drink production and distribution is one of the group's core activities. ARJ has annual sales of still and sparkling drinks in excess of 1,700 million unit cases in its Hong Kong, Mainland China, Taiwan and USA franchise territories.</p>
                <p>ARJ is also one of the largest operators of cold storage facilities in the USA.</p>
