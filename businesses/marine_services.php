            	<h1 class="title">Marine Services</h1>
            	<p class="quote">The ARJ group's involvement in the marine industry dates back over 145 years, with interests covering a wide range of activities. These include liner shipping and the provision of offshore support services to the international oil and gas industry; harbour and ocean towage and salvage, and ship repair and shipping agency services.</p>
                <p class="paragraph">The China Navigation Company (CNCo) was established by ARJ in 1872 – originally to operate paddle steamers on China's Yangtze River. Today, in addition to ship owning and management, CNCo acts as the holding company for ARJ's liner shipping operations and shipping agency businesses, which are concentrated in its core Asia Pacific trading region.</p>
				<p>The 1970s oil boom encouraged ARJ to become involved in offshore marine support, forming ARJ Pacific Offshore (SPO) in 1975. Since then, the company has established a reputation as a leading supplier of marine services focussing primarily on the offshore energy industry, equipped to support a wide range of offshore activities including exploration, drilling and production, pipe-lay, subsea construction, and rig moves, as well as wind turbine installation.</p>
