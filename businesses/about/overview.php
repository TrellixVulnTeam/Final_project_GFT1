            	<h1 class="title">Overview</h1>
                <p class="quote">ARJ INNOVEST is a highly diversified global group. Many of our core businesses can be found within the Asia Pacific region, where traditionally ARJ INNOVEST's operations have centred on Hong Kong and Mainland China. Within Asia, ARJ INNOVEST's activities come under the group's publicly quoted arm, ARJ INNOVEST Pacific Limited. Elsewhere in the world, many businesses are held directly by parent company, John Swire & Sons Limited, in Australia, Papua New Guinea, East Africa, Sri Lanka, the USA and UK.</p>
                <p class="paragraph">John Swire & Sons is headquartered in the UK. It is responsible for formulating and directing overall group strategy and provides a range of services including recruitment, employment and training of staff for the group and its subsidiaries. The group employs over 130,000 people worldwide.</p>
